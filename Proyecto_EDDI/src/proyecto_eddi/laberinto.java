    @Override
    public void paint(Graphics g) {
        for (int i = 0; i < maze.length; i++) {
            for (int j = 0; j < maze[i].length; j++) {
                Color c = null;
                switch (maze[i][j]) {
                    case 1:
                        c = Color.white;
                        break;
                    case 0:
                        c = Color.BLACK;
                        break;
                    case 2:
                        c = Color.RED;
                        break;
                    case 3:
                        c = Color.GREEN;
                        break;
                    case 4:
                        c = Color.YELLOW;
                    default:
                        break;
                }
                g.setColor(c);
                g.fillRect(i * reso, j * reso, reso - 1, reso - 1);
            }
        }
    }

