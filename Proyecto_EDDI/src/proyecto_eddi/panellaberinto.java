    @Override
    public void paint(Graphics g) {
        for (int i = 0; i < maze.length; i++) {
            for (int j = 0; j < maze[i].length; j++) {
                Color c = null;
                if (maze[i][j] == 1) {
                    c = Color.white;
                } else if (maze[i][j] == 0) {
                    c = Color.BLACK;
                }
                if (i == 19 && j == 30) {
                    c = Color.GREEN;
                } else if (i == 1 && j == 0) {
                    c = Color.RED;
                }
                g.setColor(c);
                g.fillRect(i * reso, j * reso, reso - 1, reso - 1);
            }
        }
    }


